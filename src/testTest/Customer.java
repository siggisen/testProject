package testTest;

public class Customer {
	private String customerID;
	private String customerName;
	
	private int customerSalary;
	
	private String customerAddress;
	
	
	public String getCustomerID() {
		return customerID;
	}
	public void setCustomerID(String customerName) {
		this.customerID = customerID;
	}
	public String getCustomerName() {
		return customerName;
	}
	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}
	public int getCustomerSalary() {
		return customerSalary;
	}
	public void setCustomerSalary(int customerSalary) {
		this.customerSalary = customerSalary;
	}
	
	public String setCustomerAddress (String customerAddress) {
		this.customerAddress = customerAddress;
		
	}
	
	public void getCustomerAddress() {
		return customerAddress;
		
		
	}

}
